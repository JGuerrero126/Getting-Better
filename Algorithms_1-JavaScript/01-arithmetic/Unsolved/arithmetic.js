// All the functions in this algorithm are incredibly simple, it's all just basic arithmetic but it actually is the building block of a whole lot of code.

// This function takes two numbers and returns the sum after they have been added together
var add = function (num1, num2) {
  return num1 + num2;
};

// This function subtracts one number by another number and returns the remainder.
var subtract = function (num1, num2) {
  return num1 - num2;
};

// This function multiplies two numbers together and returns the total.
var multiply = function (num1, num2) {
  return num1 * num2;
};

// This function divides a number by a second number and returns the result.
var divide = function (num1, num2) {
  return num1 / num2;
};
